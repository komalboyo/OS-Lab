/*Write a multithreaded program that generates the Fibonacci series. The program should
work as follows: The user will enter on the command line the number of Fibonacci numbers
that the program is to generate. The program then will create a separate thread that will
generate the Fibonacci numbers, placing the sequence in data that is shared by the threads
(an array is probably the most convenient data structure). When the thread finishes
execution the parent will output the sequence generated by the child thread. Because the
parent thread cannot begin outputting the Fibonacci sequence until the child thread finishes,
this will require having the parent thread wait for the child thread to finish.*/

#include <pthread.h>
#include <stdio.h>
#include <stdlib.h>

int fib[100];
void* child_thread(void* param) {
    int id = (long)param;
    fib[id] = (id < 2) ? id : fib[id - 1] + fib[id - 2];
    return 0;
}

int main() {
    printf("Enter the value of n: ");
    int n;
    scanf("%d", &n);
    pthread_t thread[100];
    for (int i = 0; i < n; i++)
        pthread_create(&thread[i], 0, child_thread, (void*)(long)i);
    for (int i = 0; i < n; i++)
        pthread_join(thread[i], 0);
    for (int i = 0; i < n; i++)
        printf("%d\n", fib[i]);
}
